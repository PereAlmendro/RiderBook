import UIKit

extension UIFont {
    static func arialMT(size: CGFloat) -> UIFont? {
        return UIFont(name: "ArialMT", size: size)
    }
    
    static func arialboldMT(size: CGFloat) -> UIFont? {
        return UIFont(name: "Arial-BoldMT", size: size)
    }
}
